package Array;

public class Matrix {
    public static void main(String[] args) {
        Integer[][] matrix = {{1,2,3},{4,5,6},{7,8,9}};
        System.out.println("Row of matrix: " +matrix.length);
        System.out.println("Column of 1 matrix: " +matrix[0].length);
        System.out.println("Column of 2 matrix: " +matrix[1].length);
        System.out.println("Column of 3 matrix: " +matrix[2].length);
    }
}
